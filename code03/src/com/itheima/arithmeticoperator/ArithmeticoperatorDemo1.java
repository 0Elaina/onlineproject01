package com.itheima.arithmeticoperator;

public class ArithmeticoperatorDemo1 {
    public static void main(String[] args) {
        //在代码中，如果有小数参与计算，结果有可能是不精确的
        System.out.println(1.1+1.01);
        System.out.println(1.1-1.01);
        System.out.println(1.1*1.01);
        System.out.println(1.1+1.1);
    }
}
