package com.itheima.arithmeticoperator;

public class ArithmeticoperatorDemo2 {
    //主入口
    //整数参与计算，结果只能得到整数
    //小数参与计算，结果可能不精确
    public static void main(String[] args) {

        //除法
        System.out.println(10/3);//3
        System.out.println(10.0/3);//3.3333333333333335

        //取模
        System.out.println();
        System.out.println();
        System.out.println();
    }
}
